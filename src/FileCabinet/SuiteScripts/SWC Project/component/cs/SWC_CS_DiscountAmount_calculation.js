    function isEmpty(obj) {
        if (obj === undefined || obj == null || obj === '') {
            return true;
        }
        if (obj.length && obj.length > 0) {
            return false;
        }
        if (obj.length === 0) {
            return true;
        }
        for (var key in obj) {
            if (hasOwnProperty.call(obj, key)) {
                return false;
            }
        }
        if (typeof (obj) == 'boolean') {
            return false;
        }
        if (typeof (obj) == 'number') {
            return false;
        }
        return true;
    }

