    function saveRecord(scriptContext) {
        debugger
        let cur = scriptContext.currentRecord;
        let subTotal = cur.getValue({fieldId: "subtotal"});//小计
        //let discountRate = cur.getValue({fieldId: "discountrate"});//折扣
        let discountRate = cur.getText({fieldId: "discountrate"});//折扣
        let discountAmount = "";
        if (!isEmpty(discountRate) && discountRate.toString().indexOf("%") != -1){
            discountAmount = discountRate;
        }else if (!isEmpty(discountRate) && !isEmpty(subTotal)) {
            let amountFloat = decimal(Number(discountRate)).div(Number(subTotal)).toNumber();
            discountAmount = decimal(Number(amountFloat)).mul(100).toNumber().toFixed(2) + "%";
        }
        cur.setText({fieldId:"custbody_swc_discount_percent",text:discountAmount,ignoreFieldChange:true});
        return  true;
    }

